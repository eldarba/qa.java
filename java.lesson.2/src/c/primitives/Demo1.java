package c.primitives;

public class Demo1 {

	public static void main(String[] args) {
		
		// literal types notation is done by a suffix
		// D - double
		// F - float
		// L - long
		
		System.out.println(3.333333333333333333333333333D);
		System.out.println(3.333333333333333333333333333F);
		System.out.println(99999999);
		System.out.println(99999999999L);
	}
}
